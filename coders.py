#Arithmetic Operators
num1 = 15
num2 = 3

sum = num1 + num2
print(f"num1 + num2: {sum}")

sub = num1 - num2
print(f"num1 - num2: {sub}")

mult = num1 * num2
print(f"num1 * num2: {mult}")

div = num1 / num2 
print(f"num1 / num2: {div}")

mod = num1 % num2
print(f"num1 % num2: {mod}")

pow = num1 ** num2
print(f"num1 ^ num2: {pow}")

#Comparison Operators
